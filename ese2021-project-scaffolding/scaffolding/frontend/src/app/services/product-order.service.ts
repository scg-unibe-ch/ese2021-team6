import { Injectable } from '@angular/core';
import { Subject } from 'rxjs';
import { Product } from '../models/product.model';

@Injectable({
  providedIn: 'root'
})
export class ProductOrderService {

  /*******************************************************************************************************************
   * VARIABLES
   ******************************************************************************************************************/

  private product: Product = new Product(0, '', '', '', 0, 0, 0, '');

  /*******************************************************************************************************************
   * OBSERVABLE SOURCES & STREAMS
   ******************************************************************************************************************/

  // Observable Sources
  private productSource = new Subject<Product>();

  // Observable Streams
  product$ = this.productSource.asObservable();

  /*******************************************************************************************************************
   * GETTERS
   ******************************************************************************************************************/

  getProduct(): Product {
    return this.product;
  }

  /*******************************************************************************************************************
   * SETTERS
   ******************************************************************************************************************/

  setProduct(newProduct: Product): void {
    this.productSource.next(newProduct);
  }

  /*******************************************************************************************************************
   * CONSTRUCTOR
   ******************************************************************************************************************/

  constructor() {
    // Observer
    this.product$.subscribe(res => this.product = res);
    this.setProduct(new Product(0, '', '', '', 0, 0, 0, ''));
  }
}
